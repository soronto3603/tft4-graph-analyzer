import { convexHull } from './geo'

test('test convex hull', () => {
  expect(convexHull([
    { x: 751.4549287228888, y: 393.3055118026757 },
    { x: 581.4907105635123, y: 198.1725988334059 },
    { x: 625.2723169809406, y: 179.50633131374863 },
    { x: 675.636235332314, y: 372.964333209625 },
    { x: 691.6132908799142, y: 303.20388623169293 }
  ]))
    .toEqual([
      { x: 581.4907105635123, y: 198.1725988334059 },
      { x: 675.636235332314, y: 372.964333209625 },
      { x: 751.4549287228888, y: 393.3055118026757 },
      { x: 625.2723169809406, y: 179.50633131374863 }
    ])
})
